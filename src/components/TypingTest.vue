        newVal.length > oldVal.length &&
        !this.currentSentenceSuccess[this.currentWordIndex][newVal.length - 1]
      ) {
        this.correctCharacter++;
        this.correctCharactersSection++;
      }
      if (
        this.firstSentence[this.currentWordIndex][newVal.length - 1] !=
          lastChar &&
        newVal.length > oldVal.length
      ) {
        this.incorrectCharacter++;
      }
    },
    manageStatisticChart(lastChar: string, newVal: string, oldVal: string) {
      if (this.nextCharIndex - 1 === 0 && this.currentWordMaximumIndex == -1) {
        if (this.statisticChart.words.length > 0) {
          this.statisticChart.continuousWPM[
            this.statisticChart.continuousWPM.length - 1
          ] = this.wpm;

          this.statisticChart.sectionWPM[
            this.statisticChart.sectionWPM.length - 1
          ] = this.wpmSection;
          this.timerSection = 0;
          this.correctCharactersSection = 0;
        }
        this.statisticChart.words.push(
          "'" + this.firstSentence[this.currentWordIndex].join("") + "'"
        );
        this.statisticChart.incorrectCharacters.push(0);
        this.statisticChart.sectionWPM.push(0);
        this.statisticChart.continuousWPM.push(0);
      }
      if (
        this.firstSentence[this.currentWordIndex][newVal.length - 1] !=
          lastChar &&
        newVal.length > oldVal.length
      ) {
        this.statisticChart.incorrectCharacters[
          this.statisticChart.incorrectCharacters.length - 1
        ]++;
      }
      if (this.currentWordMaximumIndex < newVal.length - 1) {
        this.currentWordMaximumIndex = newVal.length - 1;
      }
    },
    countDownTimer(): void {
      if (this.timerDuration > 0) {
        setTimeout(() => {
          if (this.running) {
            this.timerDuration--;
            this.timerSection++;
            this.timePast++;
            this.countDownTimer();
          }
        }, 10);
      } else {
        this.finished = true;
      }
    },
    generateSentence(): string[][] {
      const sentence: string[][] = [];
      while (
        sentence.reduce((acc, val) => acc + val.length, 0) +
          sentence.length -
          1 <
        43
      ) {
        let word =
          this.wordList[Math.floor(Math.random() * this.wordList.length) + 1];
        if (this.$store.state.simpleWordList && word.length > 6) {
          continue;
        }
        if (this.probabilityCapitalLetter >= Math.random()) {
          word = word.slice(0, 1).toUpperCase() + word.slice(1);
        }
        sentence.push(word.split(""));
      }
      return sentence;
    },
    generateStructer(arr: string[][]): boolean[][] {
      const result: boolean[][] = [];
      for (const x of arr) {
        const tmp: boolean[] = [];
        for (const _ of x) {
          tmp.push(false);
        }
        result.push(tmp);
      }
      return result;
    },
  },
  created(): void {
    this.initialize();
    this.getData();
  },
});
